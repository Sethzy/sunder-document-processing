// ============================================================================
// Streaming Event Types (for SSE first message)
// ============================================================================

/**
 * Custom metadata event sent at the end of SSE stream.
 * Contains containerId and saved file info needed for follow-up messages.
 */
export interface StreamingMetadataEvent {
  type: "metadata";
  containerId: string | null;
  savedFiles: Array<{
    fileId: string;
    filename: string;
    signedUrl: string;
  }>;
}

/**
 * Anthropic raw streaming event types (subset relevant to our use case).
 * @see https://docs.anthropic.com/en/api/messages-streaming
 */
export type AnthropicStreamEvent =
  | { type: "message_start"; message: { id: string; model: string } }
  | { type: "content_block_start"; index: number; content_block: AnthropicContentBlock }
  | { type: "content_block_delta"; index: number; delta: AnthropicStreamDelta }
  | { type: "content_block_stop"; index: number }
  | { type: "message_delta"; delta: { stop_reason: string }; usage?: { output_tokens: number } }
  | { type: "message_stop" }
  | StreamingMetadataEvent;

/**
 * Delta types for streaming content blocks.
 * Each delta type corresponds to a specific content block type.
 */
export type AnthropicStreamDelta =
  | { type: "text_delta"; text: string }
  | { type: "thinking_delta"; thinking: string }
  | { type: "input_json_delta"; partial_json: string }
  | { type: "bash_code_execution_input_delta"; input_json_delta: string }
  | { type: "text_editor_code_execution_input_delta"; input_json_delta: string };

