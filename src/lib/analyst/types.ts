/**
 * Metadata for a template file uploaded to Claude's container.
 * Only stores display info - actual file goes to Anthropic Files API.
 */
export interface UploadedFile {
  /** Original filename (e.g., "budget.xlsx") */
  name: string;
  /** File size in bytes */
  size: number;
}

/** Chat session stored in localStorage */
export interface ChatSession {
  caseId: string;
  messages: UIMessageType[];
  containerId: string | null;
  /** ISO timestamp when session started (first message sent) */
  startedAt: string;
  /** Selected tag IDs for filtering. Empty array = all tags. */
  selectedTags?: string[];
  /** Template files uploaded to Claude's container (first message only) */
  uploadedFiles?: UploadedFile[];
}

/** LocalStorage key factory */
export const CHAT_STORAGE_KEY = (caseId: string) => `analyst-chat-${caseId}`;

