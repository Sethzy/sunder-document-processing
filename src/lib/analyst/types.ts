/**
 * @fileoverview Zod schemas and TypeScript types for AI Analyst chat API.
 * Validates UIMessage format from Vercel AI SDK 5.0+.
 * @see https://ai-sdk.dev/docs/reference/ai-sdk-core/ui-message
 */

import { z } from "zod";

// ============================================================================
// Message Part Schemas (AI SDK 5.0+ UIMessage.parts)
// ============================================================================

/** Text part - the most common message part */
export const TextPartSchema = z.object({
  type: z.literal("text"),
  text: z.string(),
});

/** Reasoning/thinking part - from extended thinking */
export const ReasoningPartSchema = z.object({
  type: z.literal("reasoning"),
  text: z.string(),
});

/** File part - generated files or user uploads */
export const FilePartSchema = z.object({
  type: z.literal("file"),
  url: z.string(),
  mediaType: z.string(),
  filename: z.string().optional(),
});

/** Image part - user-attached images (base64 encoded) */
export const ImagePartSchema = z.object({
  type: z.literal("image"),
  data: z.string(), // base64 encoded
  mediaType: z.string(),
});

/** Tool invocation part - when model calls a tool */
export const ToolInvocationPartSchema = z.object({
  type: z.literal("tool-invocation"),
  toolCallId: z.string(),
  toolName: z.string(),
  state: z.enum(["call", "partial-call"]),
  args: z.record(z.string(), z.unknown()),
});

/** Tool result part - result from tool execution */
export const ToolResultPartSchema = z.object({
  type: z.literal("tool-result"),
  toolCallId: z.string(),
  state: z.enum(["result", "error"]),
  result: z.unknown(),
});

/** Dynamic tool part - AI SDK's format for tool calls with results */
export const DynamicToolPartSchema = z.object({
  type: z.literal("dynamic-tool"),
  toolCallId: z.string(),
  toolName: z.string(),
  state: z.enum([
    "input-streaming",
    "input-available",
    "approval-requested",
    "approval-responded",
    "output-available",
    "output-error",
    "output-denied",
  ]),
  input: z.unknown().optional(),
  output: z.unknown().optional(),
  errorText: z.string().optional(),
  title: z.string().optional(),
  providerExecuted: z.boolean().optional(),
});

/** Union of all known message part types */
export const MessagePartSchema = z.discriminatedUnion("type", [
  TextPartSchema,
  ReasoningPartSchema,
  FilePartSchema,
  ImagePartSchema,
  ToolInvocationPartSchema,
  ToolResultPartSchema,
  DynamicToolPartSchema,
]);

// ============================================================================
// UIMessage Schema
// ============================================================================

/** UIMessage schema - validates incoming messages from client */
export const UIMessageSchema = z.object({
  id: z.string(),
  role: z.enum(["system", "user", "assistant"]),
  parts: z.array(MessagePartSchema),
  metadata: z.record(z.string(), z.unknown()).optional(),
});

export type UIMessageType = z.infer<typeof UIMessageSchema>;

// ============================================================================
// Chat Request Schema
// ============================================================================

/** Schema for template files uploaded to container */
export const TemplateFileSchema = z.object({
  name: z.string(),
  content: z.string(), // base64 encoded
  mimeType: z.string(),
});

/** Request body schema for /api/chat endpoint */
export const ChatRequestSchema = z.object({
  messages: z.array(UIMessageSchema).min(1, "At least one message required"),
  caseId: z.string().uuid("Invalid case ID format"),
  containerId: z.string().nullish(), // nullish = null | undefined
  selectedTags: z.array(z.string()).optional(),
  /** Template files to upload on first message (Excel, Word, PDF, etc.) */
  templateFiles: z.array(TemplateFileSchema).optional(),
  /** Enable extended thinking - gives Claude a thinking budget for deeper reasoning */
  extendedThinking: z.boolean().optional(),
});

export type ChatRequest = z.infer<typeof ChatRequestSchema>;

// ============================================================================
// Response Types (for first message - Anthropic SDK)
// ============================================================================

/** Bash code execution output - file generated by bash execution */
export interface BashCodeExecutionOutput {
  type: "bash_code_execution_output";
  file_id: string;
  /** Original filename (may not be present in Anthropic response) */
  filename?: string;
  /** Supabase signed URL for download (added by our API after saving to storage) */
  downloadUrl?: string;
}

/** Anthropic content block types returned by first message API */
export type AnthropicContentBlock =
  | { type: "text"; text: string }
  | { type: "thinking"; thinking: string }
  | {
      type: "server_tool_use";
      id: string;
      name: string;
      input: Record<string, unknown>;
    }
  | {
      type: "server_tool_result";
      tool_use_id: string;
      content: unknown;
      is_error?: boolean;
    }
  | {
      type: "bash_code_execution_tool_use";
      id: string;
      input: { command: string };
    }
  | {
      type: "bash_code_execution_tool_result";
      tool_use_id?: string;
      content: {
        type: "bash_code_execution_result" | "bash_code_execution_error";
        stdout?: string;
        stderr?: string;
        return_code?: number;
        content?: BashCodeExecutionOutput[];
      };
    }
  | {
      type: "text_editor_code_execution_tool_use";
      id: string;
      input: { command: string; path?: string; file_text?: string; new_str?: string };
    }
  | {
      type: "text_editor_code_execution_tool_result";
      tool_use_id?: string;
      content: {
        type: "text_editor_code_execution_result" | "text_editor_code_execution_view_result" | "text_editor_code_execution_create_result";
        file_type?: string;
        content?: string;
        numLines?: number;
      };
    };

/** Response from first message API call (Anthropic SDK, non-streaming) */
export interface FirstMessageResponse {
  id: string;
  containerId: string | null;
  content: AnthropicContentBlock[];
}

